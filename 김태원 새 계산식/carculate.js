function add(a, b){
    return a+b;
}

function sub(a, b){
    return a-b;
}

function mul(a, b){
    return a*b;
}

function div(a, b){
    return a/b;
}

function squ(a, b){
    return a**b;
}

console.log(add(55,44));
console.log(sub(33,22));
console.log(mul(11,9));
console.log(div(8,7));
console.log(squ(6,5));